    private void TTBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TTBtnActionPerformed
        // TODO add your handling code here:
        try{
            Connection conn = BTH5_C2.getMyConnection();
            String SQL = "Update KHAMBENH "
                    + "set THANHTOAN = '1' "
                    + "where MAKB = ?";
            
            PreparedStatement ps = conn.prepareStatement(SQL);
            ps.setString(1, MaKB);
            
            int rs = ps.executeUpdate();
            if(rs >= 1)
            {
                AlreadyPaid.setSelected(true);
                TTBtn.setEnabled(false);
                JOptionPane.showMessageDialog(this, "Thanh toan thanh cong");
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Thanh toan that bai");
            }
        }catch(Exception e)
        {
            System.out.print(e);
        }
        
    }//GEN-LAST:event_TTBtnActionPerformed

