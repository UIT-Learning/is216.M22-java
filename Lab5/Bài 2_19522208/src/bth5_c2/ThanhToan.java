    private void NgayKhamKeyPressed(KeyEvent evt)
    {
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            String tieuDe[] = {"Ten dich vu","So Luong","Thanh Tien"};
            DefaultTableModel tbModelDV = new DefaultTableModel();
            tbModelDV.setColumnIdentifiers(tieuDe);
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String date = sdf.format(this.NgayKham.getDate());
            String MaBN = this.MaBN.getText();
            
            String ThanhToan = "";
            try{
            String rowModel[] = new String[3];
            Connection conn = BTH5_C2.getMyConnection();
            
            String SQL1 = "Select MAKB, YEUCAUKHAM, KETLUAN, THANHTOAN from KHAMBENH "
                    + "where MABN = ? AND NGAYKHAM = ?";
            
            String SQL2 = "Select DV.TENDV, TP.SOLUONG, TP.THANHTIEN from THUPHI TP, DICHVU DV "
                    + "where TP.MAKB = ? AND DV.MADV = TP.MADV";
            
            PreparedStatement ps1 = conn.prepareStatement(SQL1);
            PreparedStatement ps2 = conn.prepareStatement(SQL2);
            
            System.out.println(MaBN + date);
            
            ps1.setString(1, MaBN);
            ps1.setString(2, date);
            
            
            ResultSet rs1 = ps1.executeQuery();
            
            
            if(rs1.next())
            {
                MaKB = rs1.getString(1);
                System.out.println(MaKB);
                String YeuCauKham = rs1.getString(2);
                String KetLuan = rs1.getString(3);
                ThanhToan = rs1.getString(4);
                YEUCAUKHAM.setText(YeuCauKham);
                KETLUAN.setText(KetLuan);
                if(Integer.parseInt(ThanhToan) == 1)
                {
                    AlreadyPaid.setSelected(true);
                    TTBtn.setEnabled(false);
                }
                else
                {
                    AlreadyPaid.setSelected(false);
                    TTBtn.setEnabled(true);
                }
            }
            
            System.out.print(ThanhToan);
            
            ps2.setString(1, MaKB);
            ResultSet rs2 = ps2.executeQuery();
            
            while(rs2.next())
            {
                rowModel[0] = rs2.getString(1);
                rowModel[1] = rs2.getString(2);
                rowModel[2] = rs2.getString(3);
                tbModelDV.addRow(rowModel);
            }
            TBDV.setModel(tbModelDV);
            
            int TongTien = 0;
            int column = 2;
            int size = TBDV.getRowCount();
            for(int i =0; i < size; i++)
            {
                int ThanhTien = Integer.parseInt(TBDV.getModel().getValueAt(i, column).toString());
                TongTien = TongTien + ThanhTien;
            }
            TONGTIEN.setText(String.valueOf(TongTien));

        }catch(Exception e)
        {
            System.out.print(e);
        }
        }
    }

