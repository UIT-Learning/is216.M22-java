    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int indexTB = jTable1.getSelectedRow();
        int ret = JOptionPane.showConfirmDialog(null, "Ban co muon xoa?", "Xoa du lieu",
                    JOptionPane.YES_NO_OPTION);
        if(ret == JOptionPane.YES_OPTION)
        {
            if(indexTB < tblModelTT.getRowCount() && indexTB >= 0)
            {
                tblModelTT.removeRow(indexTB);
            }
            
            try {
                Connection conn = BaiThucHanh05.getMyConnection();
                String SQL = "delete from SANPHAM where MASP = ?";
                PreparedStatement ps = conn.prepareStatement(SQL);
                ps.setString(1, MaSP.getText());
                ps.executeUpdate();
                conn.close();
                
            } catch (SQLException ex) {
                Logger.getLogger(SanPham.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(SanPham.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int indexTB = jTable1.getSelectedRow();
        
        if(indexTB < tblModelTT.getRowCount() && indexTB >= 0)
        {
            MaSP.setText(tblModelTT.getValueAt(indexTB, 0).toString());
            TenSP.setText(tblModelTT.getValueAt(indexTB, 1).toString());
            Gia.setText(tblModelTT.getValueAt(indexTB, 2).toString());
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        int ret = JOptionPane.showConfirmDialog(null, "Ban co muon sua du lieu?", "Sua du lieu",
                    JOptionPane.YES_NO_OPTION);
        
        String MaSP = this.MaSP.getText();
        String TenSP = this.TenSP.getText();
        int Gia = Integer.parseInt(this.Gia.getText());
        
        try {
                Connection conn = BaiThucHanh05.getMyConnection();
                String SQL = "update SANPHAM "
                        + "SET TENSP = ?, GIA = ? where MASP = ?";
                PreparedStatement ps = conn.prepareStatement(SQL);
                ps.setString(1, TenSP);
                ps.setInt(2, Gia);
                ps.setString(3, MaSP);
                ps.executeUpdate();
                
                int indexTB = jTable1.getSelectedRow();
                if(indexTB < tblModelTT.getRowCount() && indexTB >= 0){
                    tblModelTT.setValueAt(this.TenSP.getText(), indexTB, 1);
                    tblModelTT.setValueAt(this.Gia.getText(), indexTB, 2);
                }  
            } catch (SQLException ex) {
                Logger.getLogger(SanPham.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(SanPham.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_jButton4ActionPerformed

