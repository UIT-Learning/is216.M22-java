    public void TaoTable(){
        tblModelTT = new DefaultTableModel();
        String tieuDe[] = {"Ma San Pham", "Ten San Pham", "Gia"};
        tblModelTT.setColumnIdentifiers(tieuDe);
        
        try{
            Connection conn = BaiThucHanh05.getMyConnection();
            String strSQL = "Select * from SANPHAM";
            String row[] = new String[3];
            PreparedStatement ps = conn.prepareStatement(strSQL);
            ResultSet rs = ps.executeQuery();
            while(rs.next())
            {
                row[0] = rs.getString(1);
                row[1] = rs.getString(2);
                row[2] = rs.getString(3);
                tblModelTT.addRow(row);
            }
        }catch(Exception e){
            System.out.print(e);
        }
        
        jTable1.setModel(tblModelTT);
        setVisible(true);
    }

